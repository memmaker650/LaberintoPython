class App:
    windowWidth = 800
    windowHeight = 600
    player = 0
    
    logging.basicConfig(filename='squidcastle.log',level=logging.DEBUG)
    logging.basicConfig(format='%(asctime)s %(message)s')
    logging.debug('This message should go to the log file')
    logging.info('So should this')
    logging.warning('And this, too')
    
    #gameIcon = pygame.image.load('carIcon.png')
    #pygame.display.set_icon(gameIcon)
 
    def __init__(self):
        self._running = True
        self.pause = False
        self._display_surf = None
        self._jugador_surf = None
        self._enemigo_surf = None
        self._block_surf = None
        self.player = Player()
        self.enemigo = Enemigo()
        self.maze = Maze()
 
    def on_init(self):
        pygame.init()
        self._display_surf = pygame.display.set_mode((self.windowWidth,self.windowHeight), pygame.HWSURFACE)
        
        pygame.display.set_caption('Laberinto SquidCastle 2020, Pandemia.')
        self._running = True
        self._jugador_surf = pygame.image.load("../Resources/player.png").convert()
        self._jugador_surf = pygame.transform.scale(self._jugador_surf, (32, 32))
        self._enemigo_surf = pygame.image.load("../Resources/wilber-eeek.png").convert()
        self._block_surf = pygame.image.load("../Resources/floor.png").convert()
 
    def on_event(self, event):
        if event.type == QUIT:
            self._running = False
 
    def on_loop(self):
        pass
    
    def on_render(self):
        self._display_surf.fill((0,0,0))
        self._display_surf.blit(self._jugador_surf, (self.player.x, self.player.y))
        self._display_surf.blit(self._enemigo_surf, (self.enemigo.x, self.enemigo.y))
        self.maze.draw(self._display_surf, self._block_surf)
        pygame.display.flip()
 
    def on_cleanup(self):
        pygame.quit()
 
    def on_execute(self):
        if self.on_init() == False:
            self._running = False
 
        while( self._running ):
            pygame.event.pump()
            keys = pygame.key.get_pressed()
            
            if (keys[K_RIGHT]):
                self.player.moveRight()
 
            if (keys[K_LEFT]):
                self.player.moveLeft()
 
            if (keys[K_UP]):
                self.player.moveUp()
 
            if (keys[K_DOWN]):
                self.player.moveDown()
 
            if (keys[K_ESCAPE]):
                self._running = False

            if (keys[K_p]):
                self.pause = True
                logging.info('Tecla de juego pausado PULSADA.')
 
            self.on_loop()
            self.on_render()
        self.on_cleanup()


if __name__ == "__main__" :
    theApp = App()
    theApp.on_execute()
