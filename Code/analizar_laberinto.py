def analizar_laberinto():
    print("=== ANÁLISIS DEL LABERINTO ===")
    print(f"Tamaño del laberinto: {NUM_CASILLAS}x{NUM_CASILLAS} = {NUM_CASILLAS*NUM_CASILLAS} casillas")
    print(f"Tamaño total del array: {len(maze)} elementos")
    
    # Contar valores por tipo
    valores = {}
    for valor in maze:
        valores[valor] = valores.get(valor, 0) + 1
    
    print("\n=== DISTRIBUCIÓN DE VALORES ===")
    for valor in sorted(valores.keys()):
        print(f"Valor {valor}: {valores[valor]} veces")
    
    # Encontrar valores > 1 (paredes)
    paredes = []
    bx = 0
    by = 0
    for i in range(0, NUM_CASILLAS * NUM_CASILLAS):
        if maze[bx + (by * NUM_CASILLAS)] > 1:
            paredes.append({
                'indice': i,
                'x': bx,
                'y': by,
                'valor': maze[bx + (by * NUM_CASILLAS)],
                'pixel_x': bx * CASILLA_PIXEL,
                'pixel_y': by * CASILLA_PIXEL
            })
        
        bx = bx + 1
        if bx > NUM_CASILLAS - 1:
            bx = 0
            by = by + 1
    
    print(f"\n=== PAREDES ENCONTRADAS ({len(paredes)}) ===")
    for pared in paredes:
        print(f"Pared {pared['indice']}: valor={pared['valor']}, pos=({pared['x']},{pared['y']}), pixel=({pared['pixel_x']},{pared['pixel_y']})")
    
    # Mostrar el laberinto visualmente
    print("\n=== LABERINTO VISUAL ===")
    print("0 = vacío, 1 = suelo, 2+ = pared")
    for y in range(NUM_CASILLAS):
        fila = ""
        for x in range(NUM_CASILLAS):
            valor = maze[x + (y * NUM_CASILLAS)]
            if valor == 0:
                fila += " "
            elif valor == 1:
                fila += "."
            else:
                fila += str(valor)
        print(f"{y:2d}: {fila}")

if __name__ == "__main__":
    analizar_laberinto()
